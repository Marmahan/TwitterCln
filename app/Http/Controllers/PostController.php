    /**
     * Show the form for creating a new resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function create(Request $request)
    {

        $request->validate([
            'body' => 'required|min:3',
        ]);


        $post = new Post;
        $post->body = $request->body;

        $user = Auth::user();
        $user->posts()->save($post);
        return redirect('/home');
    }

